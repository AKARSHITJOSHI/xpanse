package org.eclipse.xpanse.api.config;

import org.springframework.context.annotation.Configuration;
import org.springframework.http.MediaType;
import org.springframework.web.servlet.config.annotation.ContentNegotiationConfigurer;
import org.springframework.web.servlet.config.annotation.WebMvcConfigurer;

/**
 * Bean to set the default content type for requests which does not have `accept` header set.
 * Without setting this, Spring MVC will return the first available content type from the list of
 * content types, and it happens to be yaml. This bean sets the default to JSON.
 */
@Configuration
public class DefaultResponseContentTypeConfig implements WebMvcConfigurer {
    @Override
    public void configureContentNegotiation(ContentNegotiationConfigurer configurer) {
        configurer.defaultContentType(MediaType.APPLICATION_JSON);
    }
}
